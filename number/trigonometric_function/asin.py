import math

a = 1
b = -1
c = 0.5
d =-0.5
e = 0.6
f = 0.7

# range must be between -1 to 1
# asin means arc(inverse ) of sine

print(" the arc sine of 1 : ",    math.asin(a))

print(" the arc sine of -1 : ",   math.asin(b))

print(" the arc sine of 0.5 : ",  math.asin(c))

print(" the arc sine of -0.5 :",  math.asin(d))

print(" the arc sine of 0.6 : ",  math.asin(e))

print(" the arc sine of 0.7 : ", math.asin(f))