import math

a = 1
b = -1
c = 0.5
d =-0.5
e = 0.6
f = 0.7

# range must be between -1 to 1
# acos means arc(inverse ) of cosine

print(" the arc cosine of 1 : ", math.acos(a))

print(" the arc cosine of -1 : ", math.acos(b))

print(" the arc cosine of 0.5 : ", math.acos(c))

print(" the arc cosine of -0.5 : ", math.acos(d))

print(" the arc cosine of 0.6 : ", math.acos(e))

print(" the arc cosine of 0.7 : ", math.acos(f))