                        # betwise operator  
                        
a = 10; b=29; c=0

c = a& b
print (" the value of c :",c,  ":" ,bin(c)) # AND                      

c = a| b                        
print (" the value of c :",c,  ":" ,bin(c))  #OR       
                
c = a^ b
print (" the value of c :",c,  ":" ,bin(c))    #XOR                   

c = ~ b
print (" the value of c :",c,  ":" ,bin(c))     #complement                   

c = a>>b
print (" the value of c :",c,  ":" ,bin(c))    #right shift
   
c = a<<b
print (" the value of c :",c,  ":" ,bin(c))     #left shift               